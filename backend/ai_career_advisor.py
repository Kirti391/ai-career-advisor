def summary_node(state: CareerState) -> dict:
    """Produce the final career summary report and overall confidence score."""
    try:
        summary: CareerSummary = get_structured_response(
            system_prompt=CAREER_SUMMARY_SYSTEM_PROMPT,
            user_prompt=build_career_summary_user_prompt(_build_full_context(state)),
            schema=CareerSummary,
        )

        # Deduct confidence proportionally to how many nodes failed upstream,
        # so a report built on partial data is never reported as fully confident.
        failed_count = len(state.get("failed_nodes", []))
        adjusted_confidence = max(0.0, summary.confidence_score - (failed_count * 10))

        return {"career_summary": summary, "confidence_score": adjusted_confidence}

    except Exception as exc:  # noqa: BLE001
        update = with_node_error_handling(NODE_NAME_SUMMARY, state, exc)
        update.setdefault("confidence_score", 0.0)
        return update

"""**Abort Node (deterministic early exit for unusable resumes)**"""

def abort_node(state: CareerState) -> dict:
    """Build a minimal, explanatory result when the pipeline cannot proceed. No LLM call needed."""
    errors = state.get("errors", [])
    reason = errors[-1] if errors else "The resume could not be processed."

    logger.info("Pipeline aborted early. Reason: %s", reason)

    summary = CareerSummary(
        headline="We couldn't complete your career analysis.",
        overview=(
            "The pipeline stopped early because we could not extract usable "
            "content from the uploaded resume. Please re-upload a text-based "
            f"PDF resume and try again. Details: {reason}"
        ),
        strengths=[],
        priority_actions=[
            "Re-upload a text-based (non-scanned) PDF resume.",
            "Ensure the file is not corrupted or password-protected.",
        ],
        confidence_score=0.0,
    )

    return {"career_summary": summary, "confidence_score": 0.0}

"""**Conditional edge routing logic**"""

from typing import Literal


def route_after_resume_parsing(state: CareerState) -> Literal["continue", "abort"]:
    """If no usable resume text exists, short-circuit straight to the abort node."""
    resume_text = state.get("resume_text", "").strip()
    if not resume_text:
        logger.warning("Routing to 'abort': no resume text available after parsing.")
        return "abort"
    return "continue"


def route_after_career_goal(state: CareerState) -> Literal["continue", "degraded"]:
    """If the career goal couldn't be resolved, route to the degraded (role-agnostic) path."""
    if state.get("career_goal") is None:
        logger.warning("Routing to 'degraded': career_goal could not be resolved.")
        return "degraded"
    return "continue"

"""**wire everything into a compiled stateGraph**"""

from langgraph.graph import END, START, StateGraph


def build_career_advisor_graph(checkpointer=None):
    """
    Construct and compile the Career Advisor StateGraph.

    Pass a `checkpointer` (e.g. `langgraph.checkpoint.memory.MemorySaver`) to
    add multi-turn memory. Use `compile(interrupt_before=[...])` to add a
    human-in-the-loop pause before any node.
    """
    graph = StateGraph(CareerState)

    graph.add_node("resume_parser", resume_parser_node)
    graph.add_node("abort", abort_node)
    graph.add_node("extract_info", extract_info_node)
    graph.add_node("career_goal_node", career_goal_node)
    graph.add_node("skill_extractor", skill_extractor_node)
    graph.add_node("required_skills_node", required_skills_node)
    graph.add_node("skill_gap_node", skill_gap_node)
    graph.add_node("roadmap", roadmap_node)
    graph.add_node("courses_node", courses_node)
    graph.add_node("projects_node", projects_node)
    graph.add_node("resume_review", resume_review_node)
    graph.add_node("interview", interview_node)
    graph.add_node("summary", summary_node)

    graph.add_edge(START, "resume_parser")

    graph.add_conditional_edges(
        "resume_parser",
        route_after_resume_parsing,
        {"continue": "extract_info", "abort": "abort"},
    )
    graph.add_edge("abort", END)

    graph.add_edge("extract_info", "career_goal_node")

    graph.add_conditional_edges(
        "career_goal_node",
        route_after_career_goal,
        {"continue": "skill_extractor", "degraded": "skill_extractor"},
    )

    graph.add_conditional_edges(
        "skill_extractor",
        lambda state: "continue" if state.get("career_goal") is not None else "degraded",
        {"continue": "required_skills_node", "degraded": "resume_review"},
    )

    graph.add_edge("required_skills_node", "skill_gap_node")
    graph.add_edge("skill_gap_node", "roadmap")
    graph.add_edge("roadmap", "courses_node")
    graph.add_edge("courses_node", "projects_node")
    graph.add_edge("projects_node", "resume_review")

    graph.add_edge("resume_review", "interview")
    graph.add_edge("interview", "summary")
    graph.add_edge("summary", END)

    return graph.compile(checkpointer=checkpointer)


career_advisor_graph = build_career_advisor_graph()
