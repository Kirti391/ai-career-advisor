"""**LLM Call Wrapper with structured output+retries**"""

import json
import re
import time
from functools import lru_cache
from typing import TypeVar

from groq import Groq
from pydantic import BaseModel

SchemaT = TypeVar("SchemaT", bound=BaseModel)

_RETRY_AFTER_PATTERN = re.compile(
    r"try again in (?:(?P<minutes>\d+)m\s*)?(?P<seconds>[\d.]+)s", re.IGNORECASE
)

MIN_SECONDS_BETWEEN_CALLS = 15.0
_last_call_timestamp: float = 0.0


class LLMGenerationError(Exception):
    """Raised when an LLM call fails after all configured retries."""


@lru_cache
def _get_client() -> Groq:
    return Groq(api_key=os.environ["GROQ_API_KEY"])


def _extract_retry_after_seconds(exc: Exception) -> float | None:
    match = _RETRY_AFTER_PATTERN.search(str(exc))
    if not match:
        return None
    minutes = float(match.group("minutes") or 0)
    seconds = float(match.group("seconds") or 0)
    return minutes * 60 + seconds


def _is_rate_limit_error(exc: Exception) -> bool:
    text = str(exc).lower()
    return "429" in text or "rate_limit_exceeded" in text or "rate limit" in text


def _strip_markdown_fences(text: str) -> str:
    """Some models wrap JSON in ```json fences even in JSON mode; strip defensively."""
    text = text.strip()
    if text.startswith("```"):
        text = text.strip("`")
        if text.lower().startswith("json"):
            text = text[4:]
    return text.strip()


def _pace_calls() -> None:
    global _last_call_timestamp
    elapsed = time.time() - _last_call_timestamp
    if elapsed < MIN_SECONDS_BETWEEN_CALLS:
        wait = MIN_SECONDS_BETWEEN_CALLS - elapsed
        print(f"[info] Pacing: waiting {wait:.1f}s before the next LLM call to stay under the TPM budget.")
        time.sleep(wait)
    _last_call_timestamp = time.time()


def get_structured_response(
    system_prompt: str,
    user_prompt: str,
    schema: type[SchemaT],
    max_tokens: int = 2000,
) -> SchemaT:
    """
    Call Groq directly (no LangChain) using native JSON mode, and validate the
    response into `schema`.

    Args:
        max_tokens: cap on completion length. Increase this for schemas with
            large expected output (e.g. LearningRoadmap) to avoid truncated,
            unparseable JSON.
    """
    client = _get_client()
    schema_json = json.dumps(schema.model_json_schema())

    full_system_prompt = (
        f"{system_prompt}\n\n"
        "Respond with ONLY a single valid JSON object — no markdown fences, "
        "no commentary, no explanation before or after — that strictly matches "
        f"this JSON schema:\n{schema_json}"
    )

    last_error: Exception | None = None
    attempts = LLM_MAX_RETRIES + 1

    for attempt in range(1, attempts + 1):
        _pace_calls()
        try:
            completion = client.chat.completions.create(
                model=GROQ_MODEL,
                messages=[
                    {"role": "system", "content": full_system_prompt},
                    {"role": "user", "content": user_prompt},
                ],
                temperature=LLM_TEMPERATURE,
                max_tokens=max_tokens,
                response_format={"type": "json_object"},
            )
            raw_text = completion.choices[0].message.content
            parsed = json.loads(_strip_markdown_fences(raw_text))
            return schema.model_validate(parsed)

        except Exception as exc:  # noqa: BLE001
            last_error = exc

            if attempt == attempts:
                print(f"[error] LLM call failed permanently for {schema.__name__}: {exc}")
                break

            if _is_rate_limit_error(exc):
                wait_seconds = _extract_retry_after_seconds(exc)
                if wait_seconds is None:
                    wait_seconds = 2.0 * attempt
                wait_seconds = min(wait_seconds + 1.0, 60.0)
                print(
                    f"[warn] Rate limited on {schema.__name__} "
                    f"(attempt {attempt}/{attempts}); waiting {wait_seconds:.1f}s before retrying."
                )
                time.sleep(wait_seconds)
            else:
                print(f"[warn] LLM call failed (attempt {attempt}/{attempts}) for {schema.__name__}: {exc}")
                time.sleep(1.5 * attempt)

    raise LLMGenerationError(
        f"Failed to get a valid '{schema.__name__}' response from the LLM after {attempts} attempt(s)."
    ) from last_error

