"""**Node 6:skill_gap**"""

NODE_NAME_SKILL_GAP = "skill_gap"


def skill_gap_node(state: CareerState) -> dict:
    """Compare current vs. required skills to compute the skill gap."""
    try:
        current_skills = state.get("current_skills", [])
        required_skills = state.get("required_skills", [])
        required_data = state.get("required_skills_data")

        if not required_skills:
            raise ValueError("Cannot compute skill gap: no required skills were resolved.")

        good_to_have = required_data.good_to_have_skills if required_data else []
        must_have = required_data.must_have_skills if required_data else required_skills

        gap: SkillGapAnalysis = get_structured_response(
            system_prompt=SKILL_GAP_SYSTEM_PROMPT,
            user_prompt=build_skill_gap_user_prompt(
                current_skills=current_skills,
                required_skills=must_have,
                good_to_have_skills=good_to_have,
            ),
            schema=SkillGapAnalysis,
        )

        return {
            "skill_gap": gap,
            "matched_skills": gap.matched_skills,
            "missing_skills": gap.missing_skills,
            "skill_match_percentage": gap.skill_match_percentage,
        }

    except Exception as exc:  # noqa: BLE001
        update = with_node_error_handling(NODE_NAME_SKILL_GAP, state, exc)
        update.setdefault("matched_skills", state.get("matched_skills", []))
        update.setdefault("missing_skills", state.get("missing_skills", []))
        update.setdefault("skill_match_percentage", state.get("skill_match_percentage", 0.0))
        return update

