import fitz  # PyMuPDF


class PDFExtractionError(Exception):
    """Raised when a PDF file cannot be opened or contains no extractable text."""


def extract_text_from_pdf_bytes(file_bytes: bytes) -> str:
    if not file_bytes:
        raise PDFExtractionError("Uploaded resume file is empty.")
    try:
        document = fitz.open(stream=file_bytes, filetype="pdf")
    except Exception as exc:
        raise PDFExtractionError("The uploaded file could not be read as a valid PDF.") from exc

    pages_text = []
    try:
        for page_index in range(document.page_count):
            pages_text.append(document.load_page(page_index).get_text("text"))
    finally:
        document.close()

    full_text = "\n".join(pages_text).strip()
    if not full_text:
        raise PDFExtractionError("No extractable text was found in the resume.")
    return full_text

