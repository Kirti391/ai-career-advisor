from typing import Any


class _StubLogger:
    """Minimal print-based stand-in for the backend's real logger (utils/logger.py)."""

    def info(self, msg, *args):
        print("[info]", msg % args if args else msg)

    def warning(self, msg, *args):
        print("[warn]", msg % args if args else msg)

    def error(self, msg, *args):
        print("[error]", msg % args if args else msg)


def get_logger(name: str) -> _StubLogger:
    return _StubLogger()


logger = get_logger(__name__)


def with_node_error_handling(node_name: str, state: "CareerState", exc: Exception) -> dict[str, Any]:
    """Build a partial-state update recording a node failure, without raising."""
    logger.error("Node '%s' failed: %s", node_name, exc)
    existing_errors = list(state.get("errors", []))
    existing_failed = list(state.get("failed_nodes", []))
    existing_errors.append(f"{node_name}: {exc}")
    existing_failed.append(node_name)
    return {"errors": existing_errors, "failed_nodes": existing_failed}

